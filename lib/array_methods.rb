def using_include(array, element)
    array.include?(element) #checks if an array includes a certain element
end

def using_sort(array)
    array.sort # sorts an array
end

def using_reverse(array)
    array.reverse # reverses an array
end

def using_first(array)
    array.first # returns first element in an array
end

def using_last(array)
    array.last # returns last elements in an array
end

def using_size(array)
    array.size # returns how many elements are in an array
end
